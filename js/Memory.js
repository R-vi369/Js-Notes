//  stack and heap memory

// stack (primitive)

// The stack is a region of memory used for managing function call information and local variables
// It follows the Last-In-First-Out (LIFO) principle, meaning that the last item pushed onto the stack is the first item to be popped off.
// The stack is typically used for small, fixed-size data structures and has a limited size.

// *************************


//  heap (non-proimitive)


// The heap is a region of memory used for dynamic memory allocation, where data can be allocated and deallocated in a more flexible manner.
// Unlike the stack, the heap doesn't have a fixed size, and memory allocation and deallocation can occur at runtime.
// The heap is used for storing objects, arrays, and other complex data structures.

